		<h1 span style="color:#015601;">ICTLab Poster: SWARMS (Say and Watch: Automated image/sound Recognition for Mobile monitoring Systems)</h1>
		<p span class="meta-date">January 11, 2016</p>
		<p><strong>Abstract</strong>: The objective of this project is to obtain a flexible and real-time monitoring network, which could supplement possibly existing (fixed) networks, and could then feed a decision-support system (through models and simulations of evolution scenarios) or support an advanced visualization of the phenomenon to monitor. The main idea is to study in a real context the feasibility of a smartphone-based monitoring network, where the devices are not only used as passive sensors but can also be actively used by their owners to transmit visual, voice and textual pieces of information to the monitoring system, in order to enable stakeholders to analyze and forecast information.</p>
