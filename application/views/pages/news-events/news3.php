        <div> <b>Abstract:</b> The aim of this project is to design a model which can represent the dynamics of spreading of the dengue fever in indirect correlation with the climate and considering healthcare policies and the economic mobility of different countries. The final result of the model is to show for the concerned countries that the limitation of spreading is possible thanks to policies, but also the combination, communication, synchronisation of members of a policy is at least as crucial as policy itself. The final model of the project is made of sub-models : an epidemiological theoretic model to study the dynamic of the disease, a mobility model to study the economic mobility of countries and a coupling model adding control policies.
