		<h1>SWARMS Project</h1>
		<p><span id="first_word">SWARMS</span> (Say and Watch: Automated image/sound Recognition for Mobile monitoring Systems).</p>
		<p>The objective of this project is to obtain a flexible and real-time monitoring network, which could supplement possibly existing (fixed) networks, and could then feed a decision-support system (through models and simulations of evolution scenarios) or support an advanced visualization of the phenomenon to monitor.</p>
		<p>The main idea is to study in a real context the feasibility of a smartphone-based monitoring network, where the devices are not only used as passive sensors but can also be actively used by their owners to transmit visual, voice and textual pieces of information to the monitoring system, in order to enable stakeholders to analyze and forecast information.</p>
